grade = int(input("성적을 입력하시오:")) #성적 입력

if grade >= 90 :
    print("A학점입니다.")

elif grade >= 80 :
    print("B학점입니다.")

elif grade >= 70 :
    print("C학점입니다.")

elif grade >= 60 :
    print("D학점입니다.")

else :
    print("F학점입니다.") #if elif else 문 이용하여 성적의 구간을 나눈다.


